package command.devices;

public class GarageDoor {

    public void up() {
        System.out.println("GarageDoor is UP...");
    }

    public void down() {
        System.out.println("GarageDoor is DOWN...");
    }

    public void stop() {
        System.out.println("GarageDoor is  STOPPED...");
    }

    public void lightOn() {
        System.out.println("GarageDoor's light is ON...");
    }

    public void lightOff() {
        System.out.println("GarageDoor's light is OFF...");
    }
}
